package Demo;

public class book {

	int id;
	String name;
	String author;
	String publication;
	int count;
	double price;

	public book(int id, String name, String author, String publication, int count, double price) {
		super();
		this.id = id;
		this.name = name;
		this.author = author;
		this.publication = publication;
		this.count = count;
		this.price = price;
	}

}
